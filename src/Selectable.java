
public class Selectable {
	int x;
	int y;
	int ID;
	
	public int getX(){
		return x;
	}
	public int getY(){
		return y;
	}
	public int getID(){
		return ID;
	}
	public void setX(int newX){
		x = newX;
	}
	public void setY(int newY){
		y = newY;
	}
	public void setID(int newID){
		ID = newID;
	}
}
